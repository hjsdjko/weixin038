					this.ruleForm.yishengxingming = obj[o];
					this.ro.yishengxingming = true;
					continue;
					}
					if(o=='yishengshouji'){
					this.ruleForm.yishengshouji = obj[o];
					this.ro.yishengshouji = true;
					continue;
					}
					if(o=='shanzhang'){
					this.ruleForm.shanzhang = obj[o];
					this.ro.shanzhang = true;
					continue;
					}
					if(o=='jiage'){
					this.ruleForm.jiage = obj[o];
					this.ro.jiage = true;
